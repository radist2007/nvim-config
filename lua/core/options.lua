-- core/options.lua
vim.opt.number = true          -- Показувати номери рядків
vim.opt.relativenumber = true  -- Показувати відносні номери рядків (зручно для навігації j/k)
vim.opt.tabstop = 2            -- Розмір табуляції = 2 пробіли
vim.opt.shiftwidth = 2         -- Розмір відступу = 2 пробіли
vim.opt.expandtab = true       -- Використовувати пробіли замість символів табуляції
vim.opt.termguicolors = true   -- Увімкнути підтримку True Color в терміналі (важливо для сучасних тем)
vim.opt.mouse = "a"            -- Увімкнути підтримку миші у всіх режимах
vim.opt.clipboard = "unnamedplus" -- Використовувати системний буфер обміну (+) для копіювання/вставки
vim.opt.splitright = true      -- Нові вертикальні розділення відкривати праворуч
vim.opt.splitbelow = true      -- Нові горизонтальні розділення відкривати знизу
vim.opt.scrolloff = 8          -- Залишати 8 рядків видимості зверху/знизу від курсору при прокрутці
vim.opt.signcolumn = "yes"     -- Завжди показувати колонку для знаків (LSP діагностика, GitSigns, DAP точки зупину)
vim.opt.updatetime = 300       -- Час (мс) бездіяльності для спрацювання події CursorHold (використовується LSP, GitSigns)
vim.opt.timeoutlen = 500       -- Час (мс) очікування для послідовності клавіш (менше значення = швидша реакція)

-- Пошук:
vim.opt.ignorecase = true -- Ігнорувати регістр при пошуку.
vim.opt.smartcase = true -- Робити пошук чутливим до регістру, тільки якщо в запиті є велика літера. (Ці дві опції зазвичай використовують разом).
vim.opt.hlsearch = true -- Підсвічувати результати пошуку.
vim.opt.incsearch = true -- Показувати результати пошуку поступово під час набору запиту.


-- Редагування:
vim.opt.wrap = false -- Вимкнути перенесення довгих рядків (часто зручніше для коду).
